import type { TranslationKeys, TranslationSchema } from './types'

/**
 * Recursively creates an object with translation keys as values
 * @param obj - Object to process
 * @param prefix - Current key prefix
 * @returns Object with same structure but string keys as values
 */
function createKeysObject<T extends Record<string, any>>(
  obj: T,
  prefix = ''
): any {
  const result: any = {}
  
  for (const key in obj) {
    if (typeof obj[key] === 'object' && obj[key] !== null) {
      result[key] = createKeysObject(obj[key], `${prefix}${key}.`)
    } else {
      result[key] = `${prefix}${key}`
    }
  }
  
  return result
}

